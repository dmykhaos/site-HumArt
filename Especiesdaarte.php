                            <p class="">Arte & Cultura</p>

                            <p class="text-justify">No tocante a Arte das tradições Culturais, pode-se associar ao principio das organizações modernas da sociedade, ou o reconhecimento das nações e das patrias, tais quais ciraram de modo ou de outro, por suas formações ideologicas e sociais de forma religiosa ou positivistas, as fundamentações de ritos, rituais ( não nescessariamente religiosos, por exemplo se considerando as tradições e os costumes;), desta forma a organização e a criação de culturas dos paises, geraram valores e estes se refletem nas suas formas de expressões culturais, algumas mais comuns e outras nem tanto salientando sempre que isso é que tornam suas identidades artisticas como unicas e por vezes alternativas ou seculares.</p>

                            <p class="text-justify">(fonte: https://www.todamateria.com.br/cultura-africana/) </p>
