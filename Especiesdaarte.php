                            <h2 class="text-center">O que é Arte</h2>
                            <p class="text-center">O que é História da Arte?</p>
                            <p class="text-justify">A história da arte é um área do conhecimento que analisa os estilos artísticos, suas modificações, obras de arte, artistas e o valor estético das obras produzidas. Esse estudo é realizado de acordo com o cenário social/político/religioso que a sociedade viveu ou vive em determinado período. Outras ciências auxiliam nessas análises tais como a arqueologia, história, paleografia, filosofia, sociologia, etc.</p>

                            <p class="text-justify">É uma disciplina que está no plano de aula de alguns cursos superiores e também como curso de graduação para aqueles que desejam se formar na área e exercer profissão de curador, crítico de arte (realiza uma análise sobre as obras, artistas ou expositores) ou gestor de museus, galerias, centros culturais e escolas de arte.</p>
