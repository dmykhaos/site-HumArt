                            <p class="text-justify">Cultura Africana</p>
                            <p class="text-justify">A cultura africana deve ser observada sempre no plural, haja vista sua existência milenar e sua vasta diversidade. Cumpre lembrar que a África não é um país.</p>

                            <p class="text-justify">A arqueologia aponta a África como o território habitado há mais tempo no planeta. Isso resultou na profusão de idiomas com mais de mil línguas, religiões, regimes políticos, condições materiais de habitação e atividades econômicas.</p>

                            <p class="text-justify">Atualmente, o continente africano ocupa um quinto da Terra, com mais de 50 países e quase 1 bilhão de habitantes.</p>

                            <p class="text-justify">Etnocentrismo, Eurocentrismo e Culturas Africanas</p>
                            <p class="text-justify">É fato conhecido que a história africana foi escrita e contada pelos colonizadores europeus.</p>

                            <p class="text-justify">Os viajantes, missionários e dirigentes coloniais foram os responsáveis pelos primeiros relatos acerca da cultura dos povos africanos.</p>

                            <p class="text-justify">Assim, além de serem capturados para alimentarem a escravidão colonial, estes povos foram usurpados em todos os seus direitos, incluindo o de contar a própria história.</p>

                            <p class="text-justify">O “Etnocentrismo” e o “Eurocentrismo” nas ciências europeias durante o século XIX são os responsáveis pela concepção das culturas africanas.</p>

                            <p class="text-justify">Nesse viés, elas são consideradas manifestações primitivas ou bárbaras, típicas dos primeiros estágios da civilização.</p>

                            <p class="text-justify">Hoje, com as independências dos países africanos, há um esforço de recuperação das tradições culturais africanas, bem como a constituição de uma historiografia local.</p>
